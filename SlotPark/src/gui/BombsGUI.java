    private void onFlip(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onFlip
        JToggleButton button = (JToggleButton) evt.getSource();
        String buttonname = button.getName();
        int val = bc.flip(buttonname);
        if (val == 0) {
            tfScore.setText("Game Over (" + score + ")");
            score = 1;
            button.setText("");
            button.setIcon(new ImageIcon(System.getProperty("user.dir") + File.separator + "src" + File.separator + "images" + File.separator + "Bomb.png"));
            button.setBackground(Color.RED);
            bc.gameOver();
            btNew.setEnabled(true);
        } else if (val == -1) {
            tfScore.setText("Gewonnen (" + score + ")");
            bc.gameOver();
            btNew.setEnabled(true);
        } else {
            score = score + val;
            s.setGeld(s.getGeld() + val);
            tfScore.setText(score + "");
            button.setText(val + "");
        }
        button.setEnabled(false);
        updateUI();
    }//GEN-LAST:event_onFlip

    private void onNewGame(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onNewGame
        btNew.setEnabled(false);
        tfScore.setText(score + "");
        bc.newGame();
        if (s.getGeld() >= 100) {
            s.setGeld(s.getGeld() - 20);
        }
        updateUI();
    }//GEN-LAST:event_onNewGame

    private void onBack(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onBack
        MenuGUI menugui = new MenuGUI();
        s.setGeld(s.getGeld());
        menugui.setS(s);
        menugui.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_onBack

    public void updateUI() {
        lbGeld.setText(String.format("Geld: %.0f Chips", s.getGeld() * 5));
    }

